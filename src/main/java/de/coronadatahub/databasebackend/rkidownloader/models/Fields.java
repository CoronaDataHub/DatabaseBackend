package de.coronadatahub.databasebackend.rkidownloader.models;

public class Fields {
    private String name;
    private String type;
    private String alias;
    private String sqlType;
    private String domain = null;
    private String defaultValue = null;

    public Fields() {
    }

    public Fields(String name, String type, String alias, String sqlType) {
        this.name = name;
        this.type = type;
        this.alias = alias;
        this.sqlType = sqlType;
    }

    public Fields(String name, String type, String alias, String sqlType, String domain, String defaultValue) {
        this.name = name;
        this.type = type;
        this.alias = alias;
        this.sqlType = sqlType;
        this.domain = domain;
        this.defaultValue = defaultValue;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }

    public String getSqlType() {
        return sqlType;
    }

    public void setSqlType(String sqlType) {
        this.sqlType = sqlType;
    }

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

    public String getDefaultValue() {
        return defaultValue;
    }

    public void setDefaultValue(String defaultValue) {
        this.defaultValue = defaultValue;
    }
}
