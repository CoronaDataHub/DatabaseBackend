package de.coronadatahub.databasebackend.coronavirusappdownloader.models.history;

import java.util.List;

public class HistoryData {

    private String id;
    private String country;
    private String name;
    private String latitude;
    private String longitude;
    private String lastUpdated;
    private List<History> history;

    public HistoryData() {
    }

    public HistoryData(String id, String country, String name, String latitude, String longitude,
                       String lastUpdated, List<History> history) {
        this.id = id;
        this.country = country;
        this.name = name;
        this.latitude = latitude;
        this.longitude = longitude;
        this.lastUpdated = lastUpdated;
        this.history = history;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLastUpdated() {
        return lastUpdated;
    }

    public void setLastUpdated(String lastUpdated) {
        this.lastUpdated = lastUpdated;
    }

    public List<History> getHistory() {
        return history;
    }

    public void setHistory(List<History> history) {
        this.history = history;
    }
}
