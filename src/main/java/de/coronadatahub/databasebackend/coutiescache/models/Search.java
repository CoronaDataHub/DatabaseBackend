package de.coronadatahub.databasebackend.coutiescache.models;

public class Search {

    private String lat;
    private String lon;

    public Search() {
    }

    public Search(String lat, String lon) {
        this.lat = lat;
        this.lon = lon;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLon() {
        return lon;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }
}
